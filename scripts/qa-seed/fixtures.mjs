export function makeAvatarDataUrl(name, bg = '0f172a', fg = 'f8fafc') {
    const initials = name
        .split(/\s+/)
        .map((part) => part[0]?.toUpperCase() ?? '')
        .join('')
        .slice(0, 2)

    const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="128" height="128" viewBox="0 0 128 128"><rect width="128" height="128" rx="28" fill="#${bg}"/><text x="64" y="72" font-size="42" text-anchor="middle" font-family="Arial, sans-serif" font-weight="700" fill="#${fg}">${initials}</text></svg>`
    return `data:image/svg+xml;utf8,${encodeURIComponent(svg)}`
}

export function makePlaceholderImage(label, bg, fg, size = '600x600') {
    return `https://placehold.co/${size}/${bg}/${fg}.png?text=${encodeURIComponent(label)}`
}
